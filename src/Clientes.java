    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila = mostrarCliente.getSelectedRow();
        if (fila >= 0) {
            IDCLIENTE.setText(mostrarCliente.getValueAt(fila, 0).toString());
            NOMBRE.setText(mostrarCliente.getValueAt(fila, 3).toString());
            APELLIDO.setText(mostrarCliente.getValueAt(fila, 4).toString());
            TELEFONO.setText(mostrarCliente.getValueAt(fila, 5).toString());
            CODRUTINA.setSelectedItem(mostrarCliente.getValueAt(fila, 2).toString());
            EMAIL.setText(mostrarCliente.getValueAt(fila, 1).toString());
        } else {
            JOptionPane.showMessageDialog(null, "Fila no seleccionada");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        int fila = mostrarCliente.getSelectedRow();
        String valor = mostrarCliente.getValueAt(fila, 0).toString();
        if (fila >= 0) {

            try {
                PreparedStatement pps = cn.prepareStatement("DELETE FROM CLIENTES WHERE IDCLIENTE='"+valor+"'");
                pps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Datos Eliminados");
                limpiar();
                mostrarClientesActivos();

            } catch (SQLException ex) {
                Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);

            }

      
    }//GEN-LAST:event_btnEliminarActionPerformed
    }
