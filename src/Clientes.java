    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        try {
            PreparedStatement pps = cn.prepareStatement("{call CRUD.INSERTAR_CLIENTE (?,?,?,?,?,?,?,?,?)}");
            pps.setString(1, IDCLIENTE.getText());
            pps.setString(2, NOMBRE.getText());
            pps.setString(3, APELLIDO.getText());
            pps.setString(4, TELEFONO.getText());
            pps.setString(5, "1");
            pps.setString(6, "1");
            pps.setString(7, "1");
            pps.setString(8, CODRUTINA.getSelectedItem().toString());
            pps.setString(9, EMAIL.getText());

            pps.executeQuery();

            JOptionPane.showMessageDialog(null, "Datos guardados");
            limpiar();
            mostrarClientesActivos();
        } catch (SQLException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);

        }

    }//GEN-LAST:event_btnAgregarActionPerformed

